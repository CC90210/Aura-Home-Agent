_SCHEMA = """
CREATE TABLE IF NOT EXISTS events (
    id            INTEGER PRIMARY KEY AUTOINCREMENT,
    timestamp     TEXT    NOT NULL,
    event_type    TEXT    NOT NULL,
    entity_id     TEXT    NOT NULL,
    old_state     TEXT,
    new_state     TEXT,
    triggered_by  TEXT    NOT NULL DEFAULT 'unknown',
    person        TEXT    NOT NULL DEFAULT 'unknown'
);

CREATE INDEX IF NOT EXISTS idx_events_entity
    ON events (entity_id, timestamp);

CREATE INDEX IF NOT EXISTS idx_events_triggered_by
    ON events (triggered_by, timestamp);

CREATE TABLE IF NOT EXISTS patterns (
    id           INTEGER PRIMARY KEY AUTOINCREMENT,
    entity_id    TEXT    NOT NULL,
    day_of_week  INTEGER NOT NULL,
    hour         INTEGER NOT NULL,
    avg_value    REAL    NOT NULL DEFAULT 0.0,
    confidence   REAL    NOT NULL DEFAULT 0.0,
    sample_count INTEGER NOT NULL DEFAULT 0,
    last_updated TEXT    NOT NULL,
    UNIQUE (entity_id, day_of_week, hour)
);

CREATE TABLE IF NOT EXISTS automation_fitness (
    id               INTEGER PRIMARY KEY AUTOINCREMENT,
    automation_id    TEXT    NOT NULL,
    generation       INTEGER NOT NULL DEFAULT 1,
    fitness_score    REAL    NOT NULL DEFAULT 1.0,
    override_count   INTEGER NOT NULL DEFAULT 0,
    activation_count INTEGER NOT NULL DEFAULT 0,
    created_at       TEXT    NOT NULL,
    UNIQUE (automation_id, generation)
);

CREATE TABLE IF NOT EXISTS suggestions (
    id              INTEGER PRIMARY KEY AUTOINCREMENT,
    automation_id   TEXT    NOT NULL,
    suggestion_type TEXT    NOT NULL,
    current_value   TEXT    NOT NULL,
    suggested_value TEXT    NOT NULL,
    reason          TEXT    NOT NULL,
    status          TEXT    NOT NULL DEFAULT 'pending',
    created_at      TEXT    NOT NULL
);
"""


class _Database:
    """
    Thin wrapper around a SQLite connection that enforces WAL mode (safe for
    concurrent readers from other services) and provides a context manager
    for transactional writes.
    """

    def __init__(self, db_path: str) -> None:
        path = Path(db_path)
        path.parent.mkdir(parents=True, exist_ok=True)
        self._path = str(path)
        self._ensure_schema()

    def _connect(self) -> sqlite3.Connection:
        conn = sqlite3.connect(self._path, timeout=10, check_same_thread=False)
        conn.row_factory = sqlite3.Row
        conn.execute("PRAGMA journal_mode=WAL;")
        conn.execute("PRAGMA foreign_keys=ON;")
        return conn

    def _ensure_schema(self) -> None:
        with self._connect() as conn:
            conn.executescript(_SCHEMA)
        log.info("Database ready at %s", self._path)

    @contextmanager
    def transaction(self) -> Generator[sqlite3.Connection, None, None]:
        """Yield an open connection inside an explicit transaction."""
        conn = self._connect()
        try:
            yield conn
            conn.commit()
        except Exception:
            conn.rollback()
            raise
        finally:
            conn.close()

    def execute(
        self, sql: str, params: tuple = ()
    ) -> list[sqlite3.Row]:
        """Read-only query; returns all rows."""
        with self._connect() as conn:
            cursor = conn.execute(sql, params)
            return cursor.fetchall()


