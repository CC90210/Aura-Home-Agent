class PatternEngine:
    """
    Records Home Assistant state-change events and mines statistical patterns.

    Usage
    -----
    Instantiate once at service startup and keep the instance alive for the
    lifetime of the process.  Call ``record_event`` on every HA event you
    want tracked.  The engine handles its own SQLite writes; all operations
    are safe to call from a single-threaded async loop or a thread pool.

    Parameters
    ----------
    config_path:
        Optional override for the YAML config file path.  Defaults to the
        ``config.yaml`` sibling of this module.
    """

    # Entity states that carry a sensible numeric representation
    _BOOLEAN_MAP: dict[str, float] = {"on": 1.0, "off": 0.0, "home": 1.0, "not_home": 0.0}

    def __init__(self, config_path: Optional[Path] = None) -> None:
        if config_path is not None:
            with config_path.open("r", encoding="utf-8") as fh:
                self._config = yaml.safe_load(fh)
        else:
            self._config = _load_config()

        self._db = _Database(self._config["database"]["path"])
        self._tracked_patterns: list[str] = self._config["tracking"]["entities"]
        self._min_samples: int = int(self._config["tracking"]["min_samples"])
        log.info(
            "PatternEngine initialised — tracking patterns: %s",
            ", ".join(self._tracked_patterns),
        )

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def record_event(
        self,
        event_type: str,
        entity_id: str,
        old_state: Optional[str],
        new_state: Optional[str],
        triggered_by: str = "unknown",
        person: str = "unknown",
    ) -> None:
        """
        Persist a single HA state-change event and update the rolling pattern
        for the affected entity.

        Parameters
        ----------
        event_type:
            Arbitrary label such as 'state_changed', 'manual_override', or
            'scene_activated'.
        entity_id:
            Full HA entity ID, e.g. 'light.living_room_leds'.
        old_state:
            Previous state string from HA.  May be None for initial events.
        new_state:
            New state string from HA.
        triggered_by:
            One of: 'user', 'automation', 'scene', 'unknown'.
        person:
            Resident identifier ('conaugh', 'adon', 'unknown').
        """
        if not self._is_tracked(entity_id):
            return

        now = datetime.now(timezone.utc)
        ts = now.isoformat()

        with self._db.transaction() as conn:
            conn.execute(
                """
                INSERT INTO events
                    (timestamp, event_type, entity_id, old_state, new_state,
                     triggered_by, person)
                VALUES (?, ?, ?, ?, ?, ?, ?)
                """,
                (ts, event_type, entity_id, old_state, new_state, triggered_by, person),
            )

        # Update the rolling pattern aggregate for this entity / slot
        self._update_pattern(entity_id, new_state, now)

        log.debug(
            "Event recorded: %s  %s → %s  (by=%s, person=%s)",
            entity_id,
            old_state,
            new_state,
            triggered_by,
            person,
        )

    def get_patterns(
        self,
        entity_id: str,
        day_of_week: Optional[int] = None,
        time_range: Optional[tuple[int, int]] = None,
    ) -> list[PatternRecord]:
        """
        Return learned patterns for ``entity_id``, optionally filtered.

        Parameters
        ----------
        entity_id:
            Full HA entity ID.
        day_of_week:
            0 (Monday) – 6 (Sunday).  None returns all days.
        time_range:
            ``(start_hour, end_hour)`` inclusive.  None returns all hours.

        Returns
        -------
        list[PatternRecord]
            Only records with ``sample_count >= min_samples`` (from config)
            are returned so callers always get statistically reliable data.
        """
        conditions = ["entity_id = ?", "sample_count >= ?"]
        params: list[int | str] = [entity_id, self._min_samples]

        if day_of_week is not None:
            conditions.append("day_of_week = ?")
            params.append(day_of_week)

        if time_range is not None:
            start_h, end_h = time_range
            conditions.append("hour BETWEEN ? AND ?")
            params.extend([start_h, end_h])

        sql = (
            "SELECT * FROM patterns WHERE "
            + " AND ".join(conditions)
            + " ORDER BY day_of_week, hour"
        )
        rows = self._db.execute(sql, tuple(params))
        return [self._row_to_pattern(r) for r in rows]

    def get_suggestions(self) -> list[AutomationSuggestion]:
        """
        Return all pending automation suggestions from the database.  These
        are generated by ``RoutineOptimizer.evolve()`` and await human review.
        """
        rows = self._db.execute(
            "SELECT * FROM suggestions WHERE status = 'pending' ORDER BY created_at DESC"
        )
        return [self._row_to_suggestion(r) for r in rows]

    def get_current_context(self) -> ApartmentContext:
        """
        Build and return an ``ApartmentContext`` snapshot using the HA REST
        API.  Falls back gracefully when HA is unreachable.
        """
        now = datetime.now(timezone.utc)
        time_of_day = _classify_time_of_day(now)
        day = now.weekday()

        persons_home = self._query_persons_home()
        activity = self._infer_activity(now, persons_home)
        weather_condition, temp_outside = self._query_weather()

        return ApartmentContext(
            timestamp=now,
            time_of_day=time_of_day,
            day_of_week=day,
            is_weekend=day >= 5,
            persons_home=persons_home,
            activity=activity,
            weather_condition=weather_condition,
            temperature_outside=temp_outside,
        )

    def evolve(self) -> int:
        """
        Trigger the Darwinian optimisation cycle.  Delegates to
        ``RoutineOptimizer`` and returns the number of new suggestions
        generated.

        Also runs ``cleanup_old_events`` at the end of each cycle to prevent
        unbounded database growth.
        """
        optimizer = RoutineOptimizer(self._config, self._db)
        result = optimizer.evolve()
        self.cleanup_old_events()
        return result

    def cleanup_old_events(self, days: int = 90) -> int:
        """
        Delete events older than ``days`` days and reclaim disk space.

        Parameters
        ----------
        days:
            Retention window in days.  Events older than this are deleted.
            Defaults to 90 days.

        Returns
        -------
        int
            Number of rows deleted.
        """
        with self._db.transaction() as conn:
            cursor = conn.execute(
                "DELETE FROM events WHERE timestamp < datetime('now', ?)",
                (f"-{days} days",),
            )
            deleted = cursor.rowcount
            conn.execute("PRAGMA wal_checkpoint(TRUNCATE)")
        if deleted:
            log.info("Cleaned up %d events older than %d days", deleted, days)
        return deleted

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _is_tracked(self, entity_id: str) -> bool:
        """Return True if ``entity_id`` matches any tracked-entity glob pattern."""
        return any(fnmatch.fnmatch(entity_id, pat) for pat in self._tracked_patterns)

    def _update_pattern(
        self, entity_id: str, new_state: Optional[str], now: datetime
    ) -> None:
        """
        Incrementally update the rolling average for entity_id at the current
        day-of-week / hour slot using Welford's online algorithm so we never
        need to re-read all historical rows.
        """
        if new_state is None:
            return

        numeric = self._state_to_numeric(new_state)
        if numeric is None:
            return

        dow = now.weekday()
        hour = now.hour
        ts = now.isoformat()

        with self._db.transaction() as conn:
            row = conn.execute(
                "SELECT avg_value, sample_count FROM patterns "
                "WHERE entity_id = ? AND day_of_week = ? AND hour = ?",
                (entity_id, dow, hour),
            ).fetchone()

            if row is None:
                conn.execute(
                    """
                    INSERT INTO patterns
                        (entity_id, day_of_week, hour, avg_value, confidence,
                         sample_count, last_updated)
                    VALUES (?, ?, ?, ?, ?, 1, ?)
                    """,
                    (entity_id, dow, hour, numeric, 0.0, ts),
                )
            else:
                n = row["sample_count"] + 1
                # Welford incremental mean
                new_avg = row["avg_value"] + (numeric - row["avg_value"]) / n
                # Confidence saturates at 1.0 after min_samples * 4 events
                confidence = min(1.0, n / (self._min_samples * 4))
                conn.execute(
                    """
                    UPDATE patterns
                       SET avg_value    = ?,
                           confidence   = ?,
                           sample_count = ?,
                           last_updated = ?
                     WHERE entity_id = ? AND day_of_week = ? AND hour = ?
                    """,
                    (new_avg, confidence, n, ts, entity_id, dow, hour),
                )

    def _state_to_numeric(self, state: str) -> Optional[float]:
        """
        Convert a HA state string to a float for averaging.

        Returns None for states that have no sensible numeric form
        (e.g. 'unavailable', 'unknown').
        """
        lower = state.lower()
        if lower in self._BOOLEAN_MAP:
            return self._BOOLEAN_MAP[lower]
        try:
            return float(state)
        except ValueError:
            return None

    def _query_persons_home(self) -> list[str]:
        """
        Query the HA REST API for the state of each configured person entity.
        Returns person IDs (from config) that are currently 'home'.
        Falls back to an empty list if HA is unreachable.
        """
        persons_cfg: list[dict] = self._config.get("persons", [])
        base = _ha_url(self._config)
        headers = _ha_headers()
        home: list[str] = []

        for person in persons_cfg:
            entity = person.get("phone_entity", "")
            if not entity:
                continue
            try:
                resp = requests.get(
                    f"{base}/api/states/{entity}",
                    headers=headers,
                    timeout=4,
                )
                if resp.status_code == 200:
                    if resp.json().get("state") == "home":
                        home.append(person["id"])
            except requests.RequestException as exc:
                log.warning("Could not query %s from HA: %s", entity, exc)

        return home

    def _infer_activity(
        self, now: datetime, persons_home: list[str]
    ) -> Activity:
        """
        Infer the current apartment activity from presence data, time of day,
        and the most recent studio-mode boolean state stored in the DB.

        This is intentionally heuristic — it improves over time as the
        pattern database accumulates evidence.
        """
        if not persons_home:
            return Activity.AWAY

        time_of_day = _classify_time_of_day(now)

        # Studio mode boolean is a reliable signal for content creation
        rows = self._db.execute(
            """
            SELECT new_state FROM events
             WHERE entity_id = 'input_boolean.studio_mode_active'
             ORDER BY timestamp DESC
             LIMIT 1
            """
        )
        if rows and rows[0]["new_state"] == "on":
            return Activity.CREATING_CONTENT

        # Goodnight scene active → sleeping
        rows = self._db.execute(
            """
            SELECT new_state FROM events
             WHERE entity_id LIKE '%goodnight%'
               AND event_type = 'scene_activated'
             ORDER BY timestamp DESC
             LIMIT 1
            """
        )
        if rows and rows[0]["new_state"] == "on":
            # Only sleep if it's plausibly bedtime
            if time_of_day in (TimeOfDay.NIGHT, TimeOfDay.MORNING):
                return Activity.SLEEPING

        # Time-of-day heuristics
        if time_of_day == TimeOfDay.MORNING:
            return Activity.WORKING
        if time_of_day == TimeOfDay.MIDDAY:
            return Activity.WORKING
        if time_of_day == TimeOfDay.AFTERNOON:
            return Activity.WORKING
        if time_of_day == TimeOfDay.EVENING:
            return Activity.RELAXING
        if time_of_day == TimeOfDay.NIGHT:
            return Activity.RELAXING

        return Activity.UNKNOWN

    def _query_weather(self) -> tuple[str, Optional[float]]:
        """
        Retrieve weather condition and outside temperature from HA's weather
        entity.  Returns ('unknown', None) when HA is unreachable.
        """
        base = _ha_url(self._config)
        headers = _ha_headers()
        try:
            resp = requests.get(
                f"{base}/api/states/weather.home",
                headers=headers,
                timeout=4,
            )
            if resp.status_code == 200:
                data = resp.json()
                condition: str = data.get("state", "unknown")
                temp: Optional[float] = data.get("attributes", {}).get("temperature")
                return condition, temp
        except requests.RequestException as exc:
            log.warning("Weather query failed: %s", exc)
        return "unknown", None

    @staticmethod
    def _row_to_pattern(row: sqlite3.Row) -> PatternRecord:
        return PatternRecord(
            entity_id=row["entity_id"],
            day_of_week=row["day_of_week"],
            hour=row["hour"],
            avg_value=row["avg_value"],
            confidence=row["confidence"],
            sample_count=row["sample_count"],
            last_updated=datetime.fromisoformat(row["last_updated"]),
        )

    @staticmethod
    def _row_to_suggestion(row: sqlite3.Row) -> AutomationSuggestion:
        return AutomationSuggestion(
            automation_id=row["automation_id"],
            suggestion_type=row["suggestion_type"],
            current_value=row["current_value"],
            suggested_value=row["suggested_value"],
            reason=row["reason"],
            status=SuggestionStatus(row["status"]),
            created_at=datetime.fromisoformat(row["created_at"]),
        )


