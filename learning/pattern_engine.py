class TimeOfDay(str, Enum):
    """Broad time-of-day category used throughout the learning system."""

    MORNING = "morning"       # 05:00 – 11:59
    MIDDAY = "midday"         # 12:00 – 13:59
    AFTERNOON = "afternoon"   # 14:00 – 17:59
    EVENING = "evening"       # 18:00 – 21:59
    NIGHT = "night"           # 22:00 – 04:59


class Activity(str, Enum):
    """Detected or inferred current apartment activity."""

    WORKING = "working"
    RELAXING = "relaxing"
    CREATING_CONTENT = "creating_content"
    EXERCISING = "exercising"
    SLEEPING = "sleeping"
    AWAY = "away"
    ENTERTAINING = "entertaining"
    UNKNOWN = "unknown"


class SuggestionStatus(str, Enum):
    PENDING = "pending"
    ACCEPTED = "accepted"
    REJECTED = "rejected"


