def _classify_time_of_day(dt: datetime) -> TimeOfDay:
    """
    Map a datetime to its ``TimeOfDay`` category.

    Night wraps past midnight (22:00 – 04:59).
    """
    h = dt.hour
    if 5 <= h <= 11:
        return TimeOfDay.MORNING
    if 12 <= h <= 13:
        return TimeOfDay.MIDDAY
    if 14 <= h <= 17:
        return TimeOfDay.AFTERNOON
    if 18 <= h <= 21:
        return TimeOfDay.EVENING
    return TimeOfDay.NIGHT  # 22:00–04:59


# ---------------------------------------------------------------------------
# CLI entry point — useful for testing the engine standalone on the Pi
# ---------------------------------------------------------------------------


def _cli() -> None:
    """
    Minimal CLI for running the evolution cycle or checking context from the
    command line.

    Usage
    -----
    python pattern_engine.py evolve
    python pattern_engine.py context
    python pattern_engine.py suggestions
    """
    import argparse

    parser = argparse.ArgumentParser(
        description="AURA Pattern Engine — standalone CLI"
    )
    parser.add_argument(
        "command",
        choices=["evolve", "context", "suggestions"],
        help="Command to run",
    )
    args = parser.parse_args()

    engine = PatternEngine()

    if args.command == "evolve":
        n = engine.evolve()
        log.info("Evolution complete — %d suggestion(s) generated.", n)

    elif args.command == "context":
        ctx = engine.get_current_context()
        awareness = ContextAwareness()
        log.info("Context: %s", awareness.describe_context())
        log.info("Raw context: %s", ctx)

    elif args.command == "suggestions":
        suggestions = engine.get_suggestions()
        if not suggestions:
            log.info("No pending suggestions.")
        for s in suggestions:
            log.info(
                "[%s] %s → %s  (%s)",
                s.automation_id,
                s.current_value,
                s.suggested_value,
                s.reason,
            )


if __name__ == "__main__":
    _cli()
