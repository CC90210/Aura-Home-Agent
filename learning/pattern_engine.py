    # Internal helpers
    # ------------------------------------------------------------------

    def _upsert_fitness_row(self, automation_id: str) -> None:
        """Ensure a current-generation fitness row exists for ``automation_id``."""
        with self._db.transaction() as conn:
            exists = conn.execute(
                """
                SELECT 1 FROM automation_fitness
                 WHERE automation_id = ?
                 LIMIT 1
                """,
                (automation_id,),
            ).fetchone()
            if not exists:
                conn.execute(
                    """
                    INSERT INTO automation_fitness
                        (automation_id, generation, fitness_score,
                         override_count, activation_count, created_at)
                    VALUES (?, 1, 1.0, 0, 0, ?)
                    """,
                    (automation_id, datetime.now(timezone.utc).isoformat()),
                )

    def _generate_mutations(
        self,
        automation_id: str,
        current_generation: int,
        fitness_score: float,
        override_count: int,
        activation_count: int,
    ) -> int:
        """
        Produce mutation suggestions for ``automation_id`` based on patterns
        in the events table.  Each suggestion is a YAML-friendly dict stored
        as strings in the ``suggestions`` table.

        Current mutation strategies
        ---------------------------
        * **time_shift** — If the automation fires at a fixed time but
          residents typically override it within a few minutes, suggest
          shifting the trigger time to match observed behaviour.
        * **brightness_adjust** — If lights are consistently manually
          adjusted after the automation sets them, suggest the observed
          preferred brightness level.
        * **new_generation** — Always bumps the generation counter so the
          next activation/override cycle is tracked fresh.

        Returns the number of suggestions written.
        """
        suggestions: list[dict] = []

        # --- Strategy 1: time shift ---
        time_suggestion = self._suggest_time_shift(automation_id, override_count)
        if time_suggestion:
            suggestions.append(time_suggestion)

        # --- Strategy 2: brightness correction ---
        brightness_suggestion = self._suggest_brightness(automation_id)
        if brightness_suggestion:
            suggestions.append(brightness_suggestion)

        # --- Fallback: generic tune suggestion if nothing specific found ---
        if not suggestions:
            suggestions.append(
                {
                    "suggestion_type": "review_required",
                    "current_value": f"fitness_score={fitness_score:.2f}",
                    "suggested_value": "manual_review",
                    "reason": (
                        f"Automation '{automation_id}' was overridden "
                        f"{override_count} of {activation_count} activations "
                        f"(fitness {fitness_score:.0%}).  No automatic mutation "
                        "strategy matched — manual review recommended."
                    ),
                }
            )

        ts = datetime.now(timezone.utc).isoformat()
        with self._db.transaction() as conn:
            for s in suggestions:
                conn.execute(
                    """
                    INSERT INTO suggestions
                        (automation_id, suggestion_type, current_value,
                         suggested_value, reason, status, created_at)
                    VALUES (?, ?, ?, ?, ?, 'pending', ?)
                    """,
                    (
                        automation_id,
                        s["suggestion_type"],
                        s["current_value"],
                        s["suggested_value"],
                        s["reason"],
                        ts,
                    ),
                )

            # Bump generation — the next activation starts a fresh cycle
            next_gen = current_generation + 1
            existing = conn.execute(
                "SELECT 1 FROM automation_fitness WHERE automation_id = ? AND generation = ?",
                (automation_id, next_gen),
            ).fetchone()
            if not existing:
                conn.execute(
                    """
                    INSERT INTO automation_fitness
                        (automation_id, generation, fitness_score,
                         override_count, activation_count, created_at)
                    VALUES (?, ?, 1.0, 0, 0, ?)
                    """,
                    (automation_id, next_gen, ts),
                )

        log.info(
            "%s — %d suggestion(s) written, bumped to generation %d",
            automation_id,
            len(suggestions),
            next_gen,
        )
        return len(suggestions)

    def _suggest_time_shift(
        self, automation_id: str, override_count: int
    ) -> Optional[dict]:
        """
        Look for manual overrides that happened shortly after this automation
        fired.  If found, compute the median gap and suggest shifting the
        automation time by that delta.
        """
        # Find events where this automation fired
        fired_rows = self._db.execute(
            """
            SELECT timestamp FROM events
             WHERE triggered_by = ?
               AND event_type = 'automation_fired'
             ORDER BY timestamp DESC
             LIMIT 50
            """,
            (automation_id,),
        )
        if len(fired_rows) < 3:
            return None

        gaps_minutes: list[float] = []
        for fired_row in fired_rows:
            fired_ts = datetime.fromisoformat(fired_row["timestamp"])
            window_end = (fired_ts + timedelta(minutes=10)).isoformat()

            override_rows = self._db.execute(
                """
                SELECT timestamp FROM events
                 WHERE triggered_by = 'user'
                   AND timestamp BETWEEN ? AND ?
                 ORDER BY timestamp ASC
                 LIMIT 1
                """,
                (fired_row["timestamp"], window_end),
            )
            if override_rows:
                override_ts = datetime.fromisoformat(override_rows[0]["timestamp"])
                gap = (override_ts - fired_ts).total_seconds() / 60.0
                gaps_minutes.append(gap)

        if len(gaps_minutes) < 3:
            return None

        # Median gap as the recommended shift
        sorted_gaps = sorted(gaps_minutes)
        median_gap = sorted_gaps[len(sorted_gaps) // 2]

        if abs(median_gap) < 1.0:
            return None  # Noise — not a meaningful shift

        direction = "later" if median_gap > 0 else "earlier"
        return {
            "suggestion_type": "time_shift",
            "current_value": "current_trigger_time",
            "suggested_value": f"shift_{direction}_{abs(median_gap):.0f}_minutes",
            "reason": (
                f"Residents consistently make manual adjustments approximately "
                f"{abs(median_gap):.0f} minutes {direction} than when "
                f"'{automation_id}' fires.  Shifting the trigger time "
                f"by {self._mutation_rate * 100:.0f}% of this gap per generation "
                "should reduce overrides."
            ),
        }

    def _suggest_brightness(self, automation_id: str) -> Optional[dict]:
        """
        Check whether lights are manually adjusted after this automation runs.
        If a consistent preferred brightness emerges, suggest it.
        """
        # Find light entities that this automation commonly affects
        fired_rows = self._db.execute(
            """
            SELECT timestamp FROM events
             WHERE triggered_by = ?
             ORDER BY timestamp DESC
             LIMIT 30
            """,
            (automation_id,),
        )
        if not fired_rows:
            return None

        brightness_values: list[float] = []
        for fired_row in fired_rows:
            window_end = (
                datetime.fromisoformat(fired_row["timestamp"]) + timedelta(minutes=5)
            ).isoformat()

            override_rows = self._db.execute(
                """
                SELECT new_state FROM events
                 WHERE triggered_by = 'user'
                   AND entity_id LIKE 'light.%'
                   AND timestamp BETWEEN ? AND ?
                 ORDER BY timestamp ASC
                 LIMIT 5
                """,
                (fired_row["timestamp"], window_end),
            )
            for r in override_rows:
                try:
                    pct = float(r["new_state"])
                    if 0.0 <= pct <= 100.0:
                        brightness_values.append(pct)
                except (ValueError, TypeError):
                    pass

        if len(brightness_values) < 5:
            return None

        avg_brightness = sum(brightness_values) / len(brightness_values)
        return {
            "suggestion_type": "brightness_adjust",
            "current_value": "automation_default_brightness",
            "suggested_value": f"{avg_brightness:.0f}_percent",
            "reason": (
                f"After '{automation_id}' activates, residents manually set light "
                f"brightness to an average of {avg_brightness:.0f}% across "
                f"{len(brightness_values)} observations.  Adopting this as the "
                "new default should eliminate the follow-up adjustment."
            ),
        }

    def generate_yaml_report(self) -> str:
        """
        Produce a human-readable YAML report of all pending suggestions.
        Intended for review via the voice agent or a dashboard card.

        Returns a YAML string ready to write to a file or display in a UI.
        """
        rows = self._db.execute(
            """
            SELECT s.*, af.generation, af.fitness_score
              FROM suggestions s
              LEFT JOIN automation_fitness af
                     ON s.automation_id = af.automation_id
              WHERE s.status = 'pending'
              ORDER BY s.created_at DESC
            """
        )

        report_data: list[dict] = []
        for row in rows:
            report_data.append(
                {
                    "automation_id": row["automation_id"],
                    "generation": row["generation"],
                    "fitness_score": row["fitness_score"],
                    "suggestion_type": row["suggestion_type"],
                    "current_value": row["current_value"],
                    "suggested_value": row["suggested_value"],
                    "reason": row["reason"],
                    "created_at": row["created_at"],
                }
            )

        return yaml.dump(
            {"aura_evolution_suggestions": report_data},
            default_flow_style=False,
            allow_unicode=True,
            sort_keys=False,
        )


