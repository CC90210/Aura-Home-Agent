@dataclass(frozen=True)
class ApartmentContext:
    """Immutable snapshot of current apartment context."""

    timestamp: datetime
    time_of_day: TimeOfDay
    day_of_week: int           # 0 = Monday … 6 = Sunday
    is_weekend: bool
    persons_home: list[str]    # person IDs currently marked 'home'
    activity: Activity
    weather_condition: str     # HA weather entity state string, e.g. 'sunny'
    temperature_outside: Optional[float]  # °C, None if unavailable


@dataclass
class PatternRecord:
    """Aggregated learned pattern for a single entity / hour / day slot."""

    entity_id: str
    day_of_week: int            # 0–6
    hour: int                   # 0–23
    avg_value: float            # numeric representation of the state
    confidence: float           # 0.0–1.0, derived from sample_count
    sample_count: int
    last_updated: datetime


@dataclass
class AutomationSuggestion:
    """A proposed change to an automation generated by RoutineOptimizer."""

    automation_id: str
    suggestion_type: str        # e.g. 'adjust_time', 'adjust_brightness'
    current_value: str
    suggested_value: str
    reason: str
    status: SuggestionStatus = SuggestionStatus.PENDING
    created_at: datetime = field(default_factory=lambda: datetime.now(timezone.utc))


