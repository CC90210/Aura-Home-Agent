class ContextAwareness:
    """
    Maintains a live picture of what is happening in the apartment.

    This class is a thin convenience wrapper.  It holds a ``PatternEngine``
    instance and exposes context queries without requiring callers to
    instantiate ``PatternEngine`` directly.

    The context is rebuilt on every call to ``get_current_context`` — there
    is no caching, because Home Assistant state can change at any time.
    Callers that need a stable snapshot should store the returned
    ``ApartmentContext`` dataclass locally.
    """

    # Hour ranges that bound each TimeOfDay category (used by callers who
    # want to schedule tasks based on context categories).
    TIME_BOUNDARIES: dict[TimeOfDay, tuple[int, int]] = {
        TimeOfDay.MORNING: (5, 11),
        TimeOfDay.MIDDAY: (12, 13),
        TimeOfDay.AFTERNOON: (14, 17),
        TimeOfDay.EVENING: (18, 21),
        TimeOfDay.NIGHT: (22, 4),   # wraps midnight
    }

    def __init__(self, config_path: Optional[Path] = None) -> None:
        self._engine = PatternEngine(config_path=config_path)

    def get_current_context(self) -> ApartmentContext:
        """
        Return a fresh ``ApartmentContext`` snapshot.

        Convenience pass-through to ``PatternEngine.get_current_context()``.
        """
        return self._engine.get_current_context()

    def is_anyone_home(self) -> bool:
        """Return True if at least one resident is currently home."""
        context = self.get_current_context()
        return len(context.persons_home) > 0

    def get_active_residents(self) -> list[str]:
        """Return the list of resident IDs currently marked as home."""
        return self.get_current_context().persons_home

    def describe_context(self) -> str:
        """
        Return a single human-readable sentence summarising the current
        context.  Used by the voice agent to construct natural responses.

        Example output
        --------------
        "It's a Tuesday evening, Conaugh is home, and it looks like you're
        relaxing. Outside it's cloudy at 8°C."
        """
        ctx = self.get_current_context()
        config = self._engine._config  # noqa: SLF001 — intentional sibling access

        day_name = ctx.timestamp.strftime("%A")
        persons_cfg: list[dict] = config.get("persons", [])
        id_to_display = {p["id"]: p["display_name"] for p in persons_cfg}
        home_names = [id_to_display.get(pid, pid) for pid in ctx.persons_home]

        if not home_names:
            presence = "nobody is home"
        elif len(home_names) == 1:
            presence = f"{home_names[0]} is home"
        else:
            presence = f"{' and '.join(home_names)} are both home"

        activity_map = {
            Activity.WORKING: "you're in work mode",
            Activity.RELAXING: "you're relaxing",
            Activity.CREATING_CONTENT: "you're in content creation mode",
            Activity.EXERCISING: "you're exercising",
            Activity.SLEEPING: "everyone's asleep",
            Activity.AWAY: "the apartment is empty",
            Activity.ENTERTAINING: "you've got people over",
            Activity.UNKNOWN: "not sure what's going on",
        }
        activity_str = activity_map.get(ctx.activity, "doing something")

        weather_str = ""
        if ctx.weather_condition != "unknown":
            weather_str = f"  Outside it's {ctx.weather_condition}"
            if ctx.temperature_outside is not None:
                weather_str += f" at {ctx.temperature_outside:.0f}°C"
            weather_str += "."

        return (
            f"It's a {day_name} {ctx.time_of_day.value}, "
            f"{presence}, and {activity_str}.{weather_str}"
        )


