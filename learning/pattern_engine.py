class RoutineOptimizer:
    """
    Darwinian self-improvement engine for AURA automations.

    Each automation is modelled as an organism with a *fitness score*
    representing the fraction of activations that residents did NOT
    subsequently override.  High fitness → leave it alone.  Low fitness →
    mutate its parameters (time offsets, brightness levels, temperature
    set-points) and generate a YAML suggestion for human review.

    Generational history is preserved in ``automation_fitness`` so you can
    always see how an automation evolved and roll back to an earlier version.

    Parameters
    ----------
    config:
        Parsed learning config dict.
    db:
        Shared ``_Database`` instance (passed in from PatternEngine so both
        classes share the same on-disk file).
    """

    def __init__(self, config: dict, db: _Database) -> None:
        self._config = config
        self._db = db
        fitness_cfg = config.get("fitness", {})
        self._good_fitness: float = float(fitness_cfg.get("good_fitness", 0.8))
        self._poor_fitness: float = float(fitness_cfg.get("poor_fitness", 0.4))
        self._mutation_rate: float = float(fitness_cfg.get("mutation_rate", 0.1))

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def record_activation(self, automation_id: str) -> None:
        """
        Increment the activation count for the current generation of
        ``automation_id``.  Call this from a HA automation trigger or
        the event listener whenever an automation fires.
        """
        self._upsert_fitness_row(automation_id)
        with self._db.transaction() as conn:
            conn.execute(
                """
                UPDATE automation_fitness
                   SET activation_count = activation_count + 1
                 WHERE automation_id = ?
                   AND generation = (
                       SELECT MAX(generation) FROM automation_fitness
                        WHERE automation_id = ?
                   )
                """,
                (automation_id, automation_id),
            )

    def record_override(self, automation_id: str) -> None:
        """
        Increment the override count for the current generation and
        recalculate fitness.  An override is any manual user action that
        reverses what an automation just did (e.g. turning lights back off
        within 2 minutes of an automation turning them on).
        """
        self._upsert_fitness_row(automation_id)
        with self._db.transaction() as conn:
            conn.execute(
                """
                UPDATE automation_fitness
                   SET override_count   = override_count + 1
                 WHERE automation_id = ?
                   AND generation = (
                       SELECT MAX(generation) FROM automation_fitness
                        WHERE automation_id = ?
                   )
                """,
                (automation_id, automation_id),
            )
            # Recompute fitness score in the same transaction
            row = conn.execute(
                """
                SELECT activation_count, override_count FROM automation_fitness
                 WHERE automation_id = ?
                   AND generation = (
                       SELECT MAX(generation) FROM automation_fitness
                        WHERE automation_id = ?
                   )
                """,
                (automation_id, automation_id),
            ).fetchone()

            if row and row["activation_count"] > 0:
                fitness = 1.0 - (row["override_count"] / row["activation_count"])
                conn.execute(
                    """
                    UPDATE automation_fitness
                       SET fitness_score = ?
                     WHERE automation_id = ?
                       AND generation = (
                           SELECT MAX(generation) FROM automation_fitness
                            WHERE automation_id = ?
                       )
                    """,
                    (fitness, automation_id, automation_id),
                )

    def get_fitness(self, automation_id: str) -> Optional[float]:
        """Return the current-generation fitness score for ``automation_id``."""
        rows = self._db.execute(
            """
            SELECT fitness_score FROM automation_fitness
             WHERE automation_id = ?
             ORDER BY generation DESC
             LIMIT 1
            """,
            (automation_id,),
        )
        return rows[0]["fitness_score"] if rows else None

    def evolve(self) -> int:
        """
        Run a full evolution cycle.

        1. Load all automation fitness records.
        2. Identify poor-fitness automations (score < ``poor_fitness``).
        3. For each poor-fitness automation, compute suggested mutations and
           write them to the ``suggestions`` table.
        4. Bump the generation counter for mutated automations.

        Returns the number of new suggestions created.
        """
        log.info("Starting Darwinian evolution cycle…")

        rows = self._db.execute(
            """
            SELECT af.automation_id,
                   af.generation,
                   af.fitness_score,
                   af.override_count,
                   af.activation_count
              FROM automation_fitness af
             INNER JOIN (
                 SELECT automation_id, MAX(generation) AS max_gen
                   FROM automation_fitness
                  GROUP BY automation_id
             ) latest ON af.automation_id = latest.automation_id
                     AND af.generation    = latest.max_gen
             WHERE af.activation_count >= 5
            """
        )

        suggestion_count = 0
        for row in rows:
            score: float = row["fitness_score"]
            automation_id: str = row["automation_id"]

            if score >= self._good_fitness:
                log.debug(
                    "%s — fitness %.2f ✓ healthy, no action needed", automation_id, score
                )
                continue

            if score < self._poor_fitness:
                log.info(
                    "%s — fitness %.2f below threshold (%.2f), generating mutations",
                    automation_id,
                    score,
                    self._poor_fitness,
                )
                n = self._generate_mutations(
                    automation_id=automation_id,
                    current_generation=row["generation"],
                    fitness_score=score,
                    override_count=row["override_count"],
                    activation_count=row["activation_count"],
                )
                suggestion_count += n
            else:
                log.info(
                    "%s — fitness %.2f marginal (%.2f–%.2f), monitoring",
                    automation_id,
                    score,
                    self._poor_fitness,
                    self._good_fitness,
                )

        log.info("Evolution cycle complete — %d suggestion(s) generated", suggestion_count)
        return suggestion_count

