"""
AURA Adaptive Learning — Pattern Engine
========================================
Three cooperating classes form the core of AURA's self-improving intelligence:

  PatternEngine    — records every relevant HA state change and mines
                     statistical patterns from the accumulated history.

  RoutineOptimizer — runs a Darwinian fitness loop on automations: high
                     override rate → poor fitness → mutation (parameter
                     adjustment).  Generates YAML suggestions for human
                     review and stores a generational history.

  ContextAwareness — maintains a live picture of what's happening in the
                     apartment right now (time of day, who's home, current
                     activity, weather) so every other system can make
                     smarter decisions without duplicating HA queries.

All persistent state lives in a single SQLite database whose path is read
from config.yaml (default: /config/aura/data/patterns.db).

Typical deployment
------------------
  The engine is imported and instantiated by a lightweight daemon (or an
  existing service such as clap_listener.py) that also subscribes to the
  HA WebSocket event stream.  On each relevant state-change event the daemon
  calls ``pattern_engine.record_event(...)``; a cron job or HA automation
  calls ``routine_optimizer.evolve()`` once a week.

Environment variables (from project .env)
------------------------------------------
  HA_URL   — base URL of the Home Assistant instance
  HA_TOKEN — long-lived access token for the HA REST API
"""

